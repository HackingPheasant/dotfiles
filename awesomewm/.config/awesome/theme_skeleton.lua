-- This is intended to a "skeleton" theme where the bulk of the configuration
-- and fallbacks are set but all the important values are in a different file,
-- that is easier for a human to read, parse and fill in as required.
-- This has the side affect of making the ability to change themes a bit easier
-- (as in a single line change) and require less duplication to add new themes
-- to achive the same result as previous themes.

